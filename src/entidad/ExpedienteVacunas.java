public class ExpedienteVacunas 
{
    private int idVacuna;
    private int idExpediente;
    private String fecha;

    public int getIdVacuna()
    {
        return idVacuna;
    }// fin del metodo getIdVacuna

    public void setIdVacuna(int idVacuna) 
    {
        this.idVacuna = idVacuna;
    }// fin del metodo setIdVacuna

    public int getIdExpediente() 
    {
        return idExpediente;
    }// fin del metodo getIdExpediente

    public void setIdExpediente(int idExpediente) 
    {
        this.idExpediente = idExpediente;
    }// fin del metodo setIdExpediente

    public String getFecha() 
    {
        return fecha;
    }// fin del metodo getFecha

    public void setFecha(String fecha) 
    {
        this.fecha = fecha;
    }// fin del metodo setFecha
    
}// fin de la clase ExpedienteVacunas
