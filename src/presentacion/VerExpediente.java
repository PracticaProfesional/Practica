    // metodo que carga los datos del paciente en este formulario
    private void cargarDatosPaciente(String cedulaPaciente)
    {
        Paciente objPaciente = new Paciente();
        NegocioPaciente objNegocioPaciente = new NegocioPaciente();
        
        ResultSet rs;
        
        try
        {
            rs = objNegocioPaciente.obtenerDatosPaciente(cedulaPaciente);
            
            if (rs.next())
            {
                // damos los valores obtenidos de la base de datos al objeto Paciente(objPaciente)
                objPaciente.setNombrePaciente(rs.getString("nombrePaciente"));
                objPaciente.setApellido1(rs.getString("apellido1Paciente"));
                objPaciente.setApellido2(rs.getString("apellido2Paciente"));
                objPaciente.setIdentificacion(rs.getString("cedulaPaciente"));
                objPaciente.setTelefono(rs.getString("numeroTelefono"));
                objPaciente.setEmail(rs.getString("email"));
                objPaciente.setFechaNacimiento(rs.getDate("fechaNacimientoPaciente").toString());
                objPaciente.setNacionalidad(rs.getString("nacionalidadPaciente"));
                objPaciente.setDireccion1(rs.getString("direccion1"));
                objPaciente.setDireccion2(rs.getString("direccion2"));
                
                // obtenemos los enumerados para el sexo y el tipo del Paciente
                if (rs.getInt("sexo") == 1)
                    objPaciente.setSexo(SexoEnum.MASCULINO);
                
                else
                    objPaciente.setSexo(SexoEnum.FEMENINO);
                
                if (rs.getInt("tipo") == 1)
                    objPaciente.setTipo(TipoEnum.ADMINISTRATIVO);
                
                else
                {
                    if (rs.getInt("tipo") == 2)
                        objPaciente.setTipo(TipoEnum.ESTUDIANTE);
                    
                    else
                        objPaciente.setTipo(TipoEnum.DOCENTE);
                }// fin del else
            }// fin del if
            
            // Se cargan los componentes con el objeto Paciente(objPaciente)
            textNombre.setText(objPaciente.getNombrePaciente());
            textApellido1.setText(objPaciente.getApellido1());
            textApellido2.setText(objPaciente.getApellido2());
            textIdentificacion.setText(objPaciente.getIdentificacion());
            textTelefono.setText(objPaciente.getTelefono());
            textEmail.setText(objPaciente.getEmail());
            textFechaNac.setText(objPaciente.getFechaNacimiento());
            textNacionalidad.setText(objPaciente.getNacionalidad());
            textDireccionFamiliar.setText(objPaciente.getDireccion1());
            textDireccionLectiva.setText(objPaciente.getDireccion2());
            cbSexo.setSelectedIndex(objPaciente.getSexo().getSexo());
            cbTipo.setSelectedIndex(objPaciente.getTipo().obtenerTipoNumero());
        }// fin del try
        catch(SQLException sqle)
        {
            JOptionPane.showMessageDialog(null, "Error al conectar con la base de datos", 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }// fin del catch
    }// fin del metodo cargarDatosPaciente
