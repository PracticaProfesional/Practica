    private void llenarListaUsuarios(){
        negocio.NegocioUsuario obUsuario = new negocio.NegocioUsuario();
        listaUsuario = new LinkedList<>();
        listaUsuario = obUsuario.listaUsuarios();
        DefaultListModel model = new DefaultListModel();
        for(entidad.Usuario usuario : listaUsuario)
            model.addElement(usuario.getNombreUsuario());
        jListUsuarios.setModel(model);
    }

